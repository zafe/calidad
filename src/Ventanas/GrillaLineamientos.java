    public static void reiniciar(javax.swing.JTable jtContenidoLineamiento){
DefaultTableModel modelo = (DefaultTableModel) jtContenidoLineamiento.getModel();
while(modelo.getRowCount()>0)
    modelo.removeRow(0);
    TableColumnModel modCol = jtContenidoLineamiento.getColumnModel();
    while(modCol.getColumnCount()>0)modCol.removeColumn(modCol.getColumn(0));
}
