  private void mostrar() {
         try{
             ListaCategoria lista = new ListaCategoria(categoria.leer());
             DefaultTableModel info=new DefaultTableModel();
             info.addColumn("CategoriaId");
             info.addColumn("Descripcion");
             for(int i=0; i<lista.getListaCategoria().size();i++){
                Object[]fila=new Object[]{
                lista.getListaCategoria().get(i).getCodigo(),
                lista.getListaCategoria().get(i).getDescripcion(),
                };
                 info.addRow(fila);
            }
                 grilla.setModel(info);
        }catch(Exception a){
            a.printStackTrace();
        }

    }
