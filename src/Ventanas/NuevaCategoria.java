    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(txtc.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese un Codigo para Buscar");
        }else{
            Categoria c= new Categoria();
            c.setCodigo(Integer.parseInt(txtc.getText()));
            if(c.buscar()){
                txtd.setText(c.getDescripcion());
                txtc.enable(false);
                txtd.enable(false);
                //txts.enable(false);
                categoria = new Categoria();
                categoria.setCodigo(Integer.parseInt(txtc.getText()));
                if(categoria.buscar()){
                    filtrar();
                }
           }else{
                JOptionPane.showMessageDialog(this, "No se encontraron coincidencias. Intentelo nuevamente");
                }
            }
}//GEN-LAST:event_jButton4ActionPerformed
