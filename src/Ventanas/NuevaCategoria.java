    private void btnNuevo1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevo1ActionPerformed
          
          txtd.enable(true);
        //  txts.enable(true);
          txtc.enable(false);
          txtd.setText("");
        //  txts.setText("");
          Categoria c = new Categoria();

        try {
            Connection cn = BD.getConnection();
            Statement stmt = cn.createStatement();
            ResultSet rs = stmt.executeQuery("select max(CategoriaId) from categoria");
            while (rs.next()) {
                txtc.setText(String.valueOf(rs.getInt(1)+1));
            }
            rs.close();
            stmt.close();
            cn.close();
        } catch (Exception error) {
            System.out.println(error);
        }
    }//GEN-LAST:event_btnNuevo1ActionPerformed

    private void btnEliminar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminar1ActionPerformed
        if(txtc.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese un Codigo para Eliminar");
        }else{
            Categoria c = new Categoria();
            c.setCodigo(Integer.parseInt(txtc.getText()));
            c.bajas();
            JOptionPane.showMessageDialog(this, "La eliminacion se realizo exitosamente");
            categoria = new Categoria();
            mostrar();
             
          txtd.enable(false);
          //txts.enable(false);
          txtc.enable(false);
          txtd.setText("");
         // txts.setText("");
          txtc.setText("");

        }
}//GEN-LAST:event_btnEliminar1ActionPerformed

