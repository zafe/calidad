    private void btnGrabar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabar1ActionPerformed
        try {
            if(txtc.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Ingrese un Codigo para Grabar");
            }else{
                int codigo =Integer.parseInt( txtc.getText());
                String descripcion = txtd.getText();
                Categoria c= new Categoria();
                c.setCodigo(codigo);
                c.setDescripcion(descripcion);
                c.altas();
                JOptionPane.showMessageDialog(null,"Una nueva Categoria ha sido Registrada");
                categoria= new Categoria();
                mostrar();
              txtd.enable(false);
            //  txts.enable(false);
              txtc.enable(false);
              txtd.setText("");
            //  txts.setText("");
              txtc.setText("");
            }
      }catch( Exception ex){
            JOptionPane.showMessageDialog(null, "No se pudo cargar nueva categoria");
        }
}//GEN-LAST:event_btnGrabar1ActionPerformed

