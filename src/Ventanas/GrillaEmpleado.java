    private void jtEmpleadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtEmpleadoMouseClicked
        {
            int fila;
            fila = jtEmpleado.rowAtPoint(evt.getPoint());
            if (fila >-1)
            {
                String Legajo= String.valueOf(jtEmpleado.getValueAt(fila, 0));
                try{
                    Connection cn = BD.getConnection();
                    Statement stmt = cn.createStatement();
                    ResultSet rs = stmt.executeQuery("select max(Legajo) from Empleado where Legajo="+Legajo);
                    while (rs.next()) {
                        txtL.setText(String.valueOf(rs.getInt(1)));
                    }
                    rs.close();
                    stmt.close();
                    cn.close();
                }
                catch(Exception e){
                    System.out.println(e);
                }
            }
        }
    }//GEN-LAST:event_jtEmpleadoMouseClicked


