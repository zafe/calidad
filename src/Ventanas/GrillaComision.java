  void mostrar() {
         try{
             ListaComision lista = new ListaComision(Comision.leer());
             DefaultTableModel info=new DefaultTableModel();
             info.addColumn("ComisionId");
             info.addColumn("Nivel");
             info.addColumn("CantidadAlumno");
             info.addColumn("Ubicacion");
             info.addColumn("CantidadComisiones");
             for(int i=0; i<lista.getListaComision().size();i++){
                Object[]fila=new Object[]{
                lista.getListaComision().get(i).getComisionId(),
                lista.getListaComision().get(i).getNivel(),    
                lista.getListaComision().get(i).getCantidadAlumnos(),
                lista.getListaComision().get(i).getUbicacion(),
                lista.getListaComision().get(i).getCantidadComisiones()
                };
                 info.addRow(fila);
            }
                 grilla.setModel(info);
        }catch(Exception a){
            a.printStackTrace();
        }

    }

