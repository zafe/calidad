    private void grillaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_grillaMouseClicked
    {
    int fila;
    fila = grilla.rowAtPoint(evt.getPoint());
        if (fila >-1)
        {
            String ComisionId= String.valueOf(grilla.getValueAt(fila, 0));
            try{
                 Connection cn = BD.getConnection();
                 Statement stmt = cn.createStatement();
                 ResultSet rs = stmt.executeQuery("select max(ComisionId) from Comision where ComisionId="+ComisionId);
                 while (rs.next()) {
                                txtCod.setText(String.valueOf(rs.getInt(1)));
                                }
                 rs.close();
                 stmt.close();
                 cn.close();
            }
            catch(Exception e){
              System.out.println(e);
            }    
        } 
    }

    }//GEN-LAST:event_grillaMouseClicked

